function lookup(dict: MessageTree, path: string): string | undefined {
  const parts = path.split('.');
  let cur: string | MessageTree | undefined = dict;
  for (const p of parts) {
    if (cur === null || typeof cur !== 'object' || typeof cur === 'string') return undefined;
    cur = cur[p];
  }
  return typeof cur === 'string' ? cur : undefined;
}

export function translate(locale: Locale, key: string, params?: Record<string, string | number>): string {
  let s = lookup(messages[locale], key) ?? lookup(messages.es, key) ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      s = s.replaceAll(`{{${k}}}`, String(v));
    }
  }
  return s;
}
